/// <summary>
/// Перечисление, содержащее время для доставки товаров.
/// </summary>
public enum DeliveryTimeRange
{
    /// <summary>
    /// Диапазон времени с 9:00 до 11:00.
    /// </summary>
    Range9To11,
    /// <summary>
    /// Диапазон времени с 11:00 до 13:00.
    /// </summary>
    Range11To13,
    /// <summary>
    /// Диапазон времени с 13:00 до 15:00.
    /// </summary>
    Range13To15,
    /// <summary>
    /// Диапазон времени с 15:00 до 17:00.
    /// </summary>
    Range15To17,
    /// <summary>
    /// Диапазон времени с 17:00 до 19:00.
    /// </summary>
    Range17To19,
    /// <summary>
    /// Диапазон времени с 19:00 до 21:00.
    /// </summary>
    Range19To21
}
